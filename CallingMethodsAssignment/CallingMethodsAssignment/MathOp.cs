using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CallingMethodsAssignment
{
    internal class MathOp
    {
        // Method to add five to a number
        public int AddFive(int num)
        {
            return num + 5;
        }

        // Method to subtract five from a number
        public int SubtractFive(int num)
        {
            return num - 5;
        }

        // Method to multiply a number by five
        public int MultiplyFive(int num)
        {
            return num * 5;
        }
    }
}
